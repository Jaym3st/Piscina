#include <unistd.h>

void	ft_print_combn(int n)
{
	char	c[10];
	int		i;

	i = 0;
	c[0] = '0';
	while (i++ < n - 1)
		c[i] = c[i - 1] + 1;
	while (c[0] < '0' + 10 - n)
	{
		if (c[n - 1] > '9')
		{
			i = n - 2;
			while (c[i] >= '0' + 10 - n + i)
				i--;
			c[i]++;
			while (i++ < n - 1)
				c[i] = c[i - 1] + 1;
		}
		write(1, &c, sizeof(c));
		if (!(c[0] == '0' + 10 - n))
			write(1, ", ", 2);
		c[n - 1]++;
	}
	if (n == 1)
		write(1, "9", 1);
}
