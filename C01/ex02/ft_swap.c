#include<stdio.h>

void	ft_swap(int *a, int *b)
{
	int z;

	z = *a;
	*a = *b;
	*b = z;
}

int	main(void)
{
	int	x;
	int y;

	x = 7;
	y = 8;
	printf("x=%d y=%d\n", x, y);
	ft_swap(&x,&y);
	printf("x=%d y=%d\n", x, y);
}
